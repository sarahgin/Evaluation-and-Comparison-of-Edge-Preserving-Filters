function [scores, scoresTxt] = getFeaturesText(originalImage,...
                              smoothedImage,...
                              edgeMask, smoothMask,...
                              colorPreservationMask,...
                              dollarImage, features)
for f=1:length(features)
    feature = features{f};
    score = getFeatureScore(feature, originalImage, smoothedImage,...
        smoothMask, dollarImage);
    scores(f) = score;
    scoresTxt{f} = strcat(features{f},':',num2str(score));
end

return